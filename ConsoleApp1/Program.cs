using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ConsoleApp1
{
    internal class Program
    {
        static void Main(string[] args)
        {

            int w, a, b, c, d, e, f, g, h;
            w = int.Parse(Console.ReadLine());
            a = 0;
            b = 0;
            c = 0;
            d = 0;
            e = 0;
            f = 0;
            g = 0;
            h = 0;
            while (w > a && w > b && w > c && w > d && w > e && w > f && w > g && w > h)
            {
                Console.Write("0", w);
                a++;
                Console.WriteLine();

                Console.Write("1", w);
                b++;
                Console.WriteLine();

                Console.Write("2", w);
                c++;
                Console.WriteLine();

                Console.Write("3", w);
                d++;
                Console.WriteLine();

                Console.Write("4", w);
                e++;
                Console.WriteLine();

                Console.Write("5", w);
                f++;
                Console.WriteLine();

                Console.Write("6", w);
                g++;
                Console.WriteLine();

                Console.Write("7", w);
                h++;

                
            }




















        }

    }
    }




